double	ft_sqrt(double n)
{
	double	x;
	double	y;
	double	e;

	x = n;
	y = 1;
	e = 0.000001;
	while (x - y > e)
	{
		x = (x + y) / 2;
		y = n / x;
	}
	return (x);
}
